        <h2 class="text-center fw-bold mt-4 mb-4">Краткая историческая справка</h2>
        <p>Промывочно-пропарочная станция Новокуйбышевская была построена и пущена в эксплуатацию в июле 1954 года в комплексе с Новокуйбышевским НПЗ и предназначалась для промывки цистерн из-под темных и светлых нефтепродуктов, для последующего налива продуктов переработки нефти на заводах Куйбышевской области. Ввод в эксплуатацию Куйбышевского и Новокуйбышевского нефтеперерабатывающих заводов потребовал увеличения количества подготовки цистерн под налив нефтепродуктов, что возможно было осуществить за счет строительства промывочно-пропарочной станции.</p>
        <p>Приказом начальника Куйбышевской железной дороги №419/Н от 6 июля 1954 года было поручено начальнику Куйбышевского отделения дороги Синельникову с 10 июля 1954 года организовать подготовку цистерн под налив темных и светлых нефтепродуктов на пропарочном пункте на путях топливного склада паровозного депо. Эта дата и является днем рождения Новокуйбышевской промывочно-пропарочной станции.</p>
        <p>Первым начальником ППС был Дударев Иван Степанович.</p>
        <p>В составе промывочно-пропарочной станции была эстакада вместимостью 12 четырехосных цистерн и котельная с 3 котлами на твердом топливе. Построены производственный корпус общей площадью 902 кв.м, и локальные очистные сооружения с насосной системой перекачки стоков на нефтеперерабатывающий завод.</p>
        <p>Технология пропарки цистерн была примитивна, не автоматизирована и требовала опускания промывальщиков с дыхательным аппаратом внутрь цистерны для удаления остатков и протирания внутренности котлов.</p>
        
