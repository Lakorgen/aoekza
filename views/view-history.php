        <h2 class="text-center fw-bold mt-4 mb-4">Проблемы и реконструкция</h2>
        <p>На протяжении последних лет морально устаревшая технология и физически изношенное оборудование не позволяли осуществлять качественную обработку цистерн для перевозки нефтепродуктов. В то же время, за прошедшие с момента постройки промыво-пропарочной станции 50 лет номенклатура выпускаемой на НПЗ продукции значительно изменилась, что предъявляет дополнительные требования к качеству предоставляемого подвижного состава. Учитывая неоднократные требования природоохранных органов, существующая промыво-пропарочная станция подлежала закрытию, как не отвечающая экологическим стандартам.</p>
        <p>Уже давно назревала необходимость коренной реконструкции существующих промывочно-пропарочных станций. Постановлением Совета Министров СССР № 556 от 2 июня 1990 года Министерству путей сообщения СССР с участием Министерства химической и нефтеперерабатывающей промышленности СССР, Государственной агропромышленной ассоциации поручено разработать технико-экономическое обоснование вариантов технического перевооружения или переноса на новую площадку промыво-пропарочных станций Куйбышевской железной дороги.</p>
        <p>Трудности заключались в том, что тогда не существовало промыво-пропарочных станций, отвечающим требованиям по мощности и по автоматизированным экологически чистым технологиям не только на территории СССР, но и в Европе.</p>
        <p>По рекомендации Министерства путей сообщения к работе над технико-коммерческими предложениями в разное время были приглашены финская фирма «ХАКА», австрийская «РЕДЛЕР» и итальянская- «АНСАЛЬДО. Работы совместно с иностранными специалистами велись с августа 1990 г.</p>
        <p>28 августа 2003 года в присутствии многочисленных делегаций и прессы состоялся торжественное открытие крупнейшего комплекса по подготовке железнодорожных цистерн.</p>
        
